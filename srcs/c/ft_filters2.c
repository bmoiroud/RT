#include "rt.h"

void		ft_bw(uint32_t *img)
{
	t_color	c;
	int		i;

	i = -1;
	while (++i < (WIN_H * WIN_W))
	{
		c.c = img[i];
		c.b = c.c & 0xff;
		c.g = c.c >> 8 & 0xff;
		c.r = c.c >> 16 & 0xff;
		img[i] = 0xff << 24 | (((c.r + c.g + c.b) / 3) << 16) | (((c.r + c.g + \
									c.b) / 3) << 8) | ((c.r + c.g + c.b) / 3);
	}
}
